const HEAD_CHARACTER = '*'

// –£–∑–µ–ª –ø—Ä–µ—Ñ–∏–∫—Å–Ω–æ–≥–æ –¥–µ—Ä–µ–≤–∞
class TrieNode {
  constructor(char, isCompleteWord = false) {
    // –°–∏–º–≤–æ–ª
    this.char = char
    // –ò–Ω–¥–∏–∫–∞—Ç–æ—Ä –∑–∞–≤–µ—Ä—à–∞—é—â–µ–≥–æ —Å–∏–º–≤–æ–ª–∞
    this.isCompleteWord = isCompleteWord
    // –•—ç—à-—Ç–∞–±–ª–∏—Ü–∞ –ø–æ—Ç–æ–º–∫–æ–≤
    this.children = new HashTable()
  }

  // –î–æ–±–∞–≤–ª—è–µ—Ç –ø–æ—Ç–æ–º–∫–∞ –≤ –¥–µ—Ä–µ–≤–æ
  addChild(char, isCompleteWord = false) {
    // –î–æ–±–∞–≤–ª—è–µ–º —É–∑–µ–ª –ø—Ä–∏ –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–∏
    if (!this.hasChild(char)) {
      this.children.set(char, new  TrieNode(char, isCompleteWord))
    }

    // –ò–∑–≤–ª–µ–∫–∞–µ–º —É–∑–µ–ª
    const node = this.getChild(char)

    // –û–±–Ω–æ–≤–ª—è–µ–º —Ñ–ª–∞–≥ `isCompleteWord` –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏,
    // –Ω–∞–ø—Ä–∏–º–µ—Ä, –ø—Ä–∏ –¥–æ–±–∞–≤–ª–µ–Ω–∏–∏ —Å–ª–æ–≤–∞ "car" –ø–æ—Å–ª–µ —Å–ª–æ–≤–∞ "carpet",
    // –±—É–∫–≤—É "r" –Ω—É–∂–Ω–æ –ø–æ–º–µ—Ç–∏—Ç—å –∫–∞–∫ –∑–∞–≤–µ—Ä—à–∞—é—â—É—é
    node.isCompleteWord = node.isCompleteWord || isCompleteWord

    // –í–æ–∑–≤—Ä–∞—â–∞–µ–º —É–∑–µ–ª
    return node
  }

  // –£–¥–∞–ª—è–µ—Ç –ø–æ—Ç–æ–º–∫–∞
  removeChild(char) {
    // –ò–∑–≤–ª–µ–∫–∞–µ–º —É–∑–µ–ª
    const node = this.getChild(char)

    // –£–¥–∞–ª—è–µ–º —É–∑–µ–ª, —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏:
    // - —É –Ω–µ–≥–æ –Ω–µ—Ç –ø–æ—Ç–æ–º–∫–æ–≤
    // - node.isCompleteWord === false
    if (node && !node.isCompleteWord && !node.hasChildren()) {
      this.children.remove(char)
    }

    return this
  }

  // –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ø–æ—Ç–æ–º–∫–∞
  getChild(char) {
    return this.children.get(char)
  }

  // –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ –ø–æ—Ç–æ–º–∫–∞
  hasChild(char) {
    return this.children.has(char)
  }

  // –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ –ø–æ—Ç–æ–º–∫–æ–≤
  hasChildren() {
    return this.children.getKeys().length > 0
  }

  // –ê–≤—Ç–æ–¥–æ–ø–æ–ª–Ω–µ–Ω–∏–µ (–ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ —Å–ª–µ–¥—É—é—â–∏—Ö —Å–∏–º–≤–æ–ª–æ–≤)
  suggestChildren() {
    return [...this.children.getKeys()]
  }

  // –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –ø–æ—Ç–æ–º–∫–æ–≤ –≤ —Å—Ç—Ä–æ–∫—É
  // —Å —É–∫–∞–∑–∞–Ω–∏–µ–º –ø—Ä–∏–∑–Ω–∞–∫–∞ –∑–∞–≤–µ—Ä—à–∞—é—â–µ–≥–æ —Å–∏–º–≤–æ–ª–∞
  toString() {
    let childrenAsString = this.suggestChildren().toString()
    childrenAsString = childrenAsString ? `:${childrenAsString}` : ''
    const isCompleteString = this.isCompleteWord ? HEAD_CHARACTER : ''

    return `${this.char}${isCompleteString}${childrenAsString}`
  }
}

