// –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è —Å—Ä–∞–≤–Ω–µ–Ω–∏—è —É–∑–ª–æ–≤
class Comparator {
    constructor(fn) {
        this.compare = fn || Comparator.defaultCompare;
    }
    // –î–µ—Ñ–æ–ª—Ç–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è —Å—Ä–∞–≤–Ω–µ–Ω—è —É–∑–ª–æ–≤
    static defaultCompare(a, b) {
        if(a === b) {
            return 0;
        }
        return a < b ? - 1 : 1;
    }
    //–ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ —Ä–∞–≤–µ–Ω—Å—Ç–≤–æ 
    equal(a, b) { 
        return this.compare(a, b) === 0; 
    }
    //–ú–µ–Ω—å—à–µ —á–µ–º
    lessThan(a, b) {
        return this.compare(a, b) < 0;
    }
    //–ë–æ–ª—å—à–µ —á–µ–º
    greaterThan(a, b) {
        return this.compare(a, b) > 0;
    }
    //–ú–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ
    lessThanOrEqual(a, b) {
        return this.lessThan(a, b) || this.equal(a, b)
    }
    //–ë–æ–ª—å—à–µ –∏–ª–∏ —Ä–∞–≤–Ω–æ
    greaterThanOrEqual(a, b) {
        return this.greaterThan(a, b) || this.equal(a, b)
    }
    //–ò–Ω–≤–µ—Ä—Å–∏—è —Å—Ä–∞–≤–Ω–µ–Ω–∏—è
    reverce() {
        const original = this.compare;
        this.compare = (a, b) => original(b, a);
    }
}

